def getPredictorOdict(conf_path, model_name='./../models/latest_G.pth'):
    import os
    dirname, filename = os.path.split(os.path.abspath(__file__))
    conf_path = os.path.join(dirname, "../", conf_path)
    if not os.path.isfile(conf_path):
        raise RuntimeError("Option yaml not found: {}".format(conf_path))
    from options import options as option
    opt = option.parse(conf_path, is_train=False)
    opt = option.dict_to_nonedict(opt)
    opt = delKeyIfExist(opt, 'gpu_ids')
    opt = delKeyIfExist(opt, 'path')

    conf_name = conf_path.split("/")[-1].split(".")[0]

    opt['path'] = {
        "pretrain_model_G": "{}".format(model_name), #gs://srfl-log/{}/experiments/train/models/{}".format(conf_name, model_name)
        "load_submodule": None,
        "strict_load": True,
        "results_root": os.path.join(dirname, "../../results/{}".format(conf_name))
    }

    return opt


def delKeyIfExist(opt, key):
    if key in opt.keys():
        del opt[key]
    return opt


class Timer:
    def __init__(self, name):
        self.name = name

    def __enter__(self):
        self.start = time.time()
        return self

    def __exit__(self, *args):
        self.end = time.time()
        self.interval = self.end - self.start
        print("{}: {:.3E}s".format(self.name, self.interval))
